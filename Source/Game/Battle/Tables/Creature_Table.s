?MAGI_TABLE_COPY
	
	FGET16		B,C,BTL_TABLE_COPY_FROM
	FGET16		H,L,BTL_TABLE_COPY_TO
	
	LD		A,(BTL_AI_MAGI)	;ID
	LD		(HLI),A
	LD_HLI_BCI								;TYPE
	
	XOR		A
	LD		(HLI),A							;LVL
	LD		(HLI),A							;EXPH
	LD		(HLI),A							;EXPL
	
	;Give magi their max energy for starting, skip "eng start" stat
	INC		BC			
	
	LD_HLI_BCI								;ENGH
	LD_HLI_BCI								;ENGL
	
	DEC		BC
	DEC		BC								;GO BACK TO THE MAX STATS AND RE-COPY
	
	LD_HLI_BCI								;ENG MAXH
	LD_HLI_BCI								;ENG MAXL
	
	LD		D,12							;ATK-IMMUNE
_MID_LOOP
	LD_HLI_BCI
	DEC		D
	JR		NZ,_MID_LOOP
	
	XOR		A
	LD		(HLI),A							;STATUS
	
	LD_HLI_BCI								;PERMSTAT
	LD_HLI_BCI								;AI
	
	INC		BC								;GET BC TO CMDS
	INC		BC
	
	LD		D,8
_CMD_LOOP
	LD_HLI_BCI
	DEC		D
	JR		NZ,_CMD_LOOP					;CMD0-CMD3	
	
	RET

